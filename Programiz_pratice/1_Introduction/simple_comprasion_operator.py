x = 10
y = 12
print("The values of x and y are : ", x , y)
# Output: x > y is False
print('x > y  is', x > y)
# Output: x < y is True
print('x < y  is', x < y)
# Output: x == y is False
print('x == y is', x == y)
# Output: x != y is True
print('x != y is', x != y)
# Output: x >= y is False
print('x >= y is', x >= y)
# Output: x <= y is True
print('x <= y is', x <= y)