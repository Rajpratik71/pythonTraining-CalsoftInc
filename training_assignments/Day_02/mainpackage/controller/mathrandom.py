import math
import random

def sqrt(num):
    return math.sqrt(num)

def squareofnum(num):
    return math.pow(num,2)

def factorial(num):
    return math.factorial(num)

def cubeofnum(num):
    return math.pow(num,3)

def randomnum():
    return random.random()

def randrangeany(l1):
    return random.choice(l1)